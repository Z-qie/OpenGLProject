Engine::Engine(GLuint w, GLuint h, const char* title)
    : m_windowSystem(w, h, title)
    , m_renderer(w, h) {
#ifdef _DEBUG
    std::cout << "DEBUG MODE*********************************" << std::endl;
    std::cout << "Initializing Window..." << std::endl;
#endif
    ConnectWindowWithInput();
}

void Engine::ConnectWindowWithInput() const {
    InputSystem::SetUpWindow(m_windowSystem.m_window);

    //const auto FramebufferSizeCallback = [](GLFWwindow* window, int w, int h) { glViewport(0, 0, w, h); };
    //glfwSetFramebufferSizeCallback(m_windowSystem.m_window, FramebufferSizeCallback);

    const auto CursorPosCallback = [](GLFWwindow* window, auto xPos, auto yPos) { InputSystem::UpdateCurosr(xPos, yPos); };
    glfwSetCursorPosCallback(m_windowSystem.m_window, CursorPosCallback);

    const auto CursorScrollCallback = [](GLFWwindow* window, double xoffset, double yoffset) { InputSystem::UpdateCurosrScroll(yoffset); };
    glfwSetScrollCallback(m_windowSystem.m_window, CursorScrollCallback);
    // tell GLFW to capture our mouse
    glfwSetInputMode(m_windowSystem.m_window, GLFW_CURSOR, GLFW_CURSOR_DISABLED);
}

void Engine::Execute() {
#ifdef _DEBUG
    std::cout << "**************************************************" << std::endl;
    std::cout << "Engine initialization complete!" << std::endl;
    std::cout << "**************************************************" << std::endl;
#endif


    Timer timer;	// set up a time for frame rate monitoring
    // Init ***********************************************************************
    m_scene.Init(m_camera);
    m_renderer.Init(m_scene);


    // Main loop *******************************************************************
    while (!m_windowSystem.m_shouldWindowClose) {
        timer.Start();

#ifdef FRAME_RATE	
        timer.PrintFrameRate();
#endif
        // get delta time of last frame
        const auto deltaTime{ timer.DeltaTime() };
        InputSystem::Update();
        m_windowSystem.Update();
        m_scene.Update(deltaTime);
        m_camera.Update(deltaTime);
        m_renderer.Update(m_camera, m_scene);
        m_renderer.Render(m_camera, m_scene);
        m_windowSystem.SwapBuffers();
        timer.End();
    }

    Shutdown();
}

void Engine::Shutdown() const {
    m_renderer.Shutdown();
    m_windowSystem.Shutdown();
}



